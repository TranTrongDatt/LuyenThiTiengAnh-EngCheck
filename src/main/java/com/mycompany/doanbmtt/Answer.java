public class Answer {
    private int maDapAn;
    private String noiDung;
    private boolean dungSai;

    public Answer(int maDapAn, String noiDung, boolean dungSai) {
        this.maDapAn = maDapAn;
        this.noiDung = noiDung;
        this.dungSai = dungSai;
    }

    public int getMaDapAn() {
        return maDapAn;
    }

    public String getNoiDung() {
        return noiDung;
    }

    public boolean isDungSai() {
        return dungSai;
    }
    @Override
public String toString() {
    return "Answer{" +
            "maDapAn=" + maDapAn +
            ", noiDung='" + noiDung + '\'' +
            ", dungSai=" + dungSai +
            '}';
}
}
